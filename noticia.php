                        <td><p>Responda rápido: você consegue imaginar outra atriz interpretando Daenerys Targaryen em Game of Thrones?</p>

                            <p>Todo mundo sabe quem é Emilia Clarke por causa da série de fantasia da HBO. Afinal, fora os papéis de Sarah Connor em O Exterminador do Futuro: Gênesis e Louise em Como Eu Era Antes de Você — longa que estreia dia 16 de junho —, a jovem de 29 anos não tem muitas produções de peso na carreira. Sem contar que sua performance como Khaleesi é memorável e é difícil imaginar outra atriz em seu lugar.</p>

                            <p>Mas, acredite, a personagem nem sempre foi intepretada por Clarke. No primeiro episódio piloto que foi produzido — que foi um desastre, segundo os showrunners David Benioff e D.B. Weiss, tendo que ser completamente refeito, com algumas substituições de elenco — a Mãe dos Dragões seria vivida por ninguém mais ninguém menos do que Tamzin Merchant.</p>

                            <p>Quem?! Merchant é conhecida por atuar nas séries The Tudors e Salem, e no longa Jane Eyre. Seu forte, pelo jeito, são personagens de época. Aliás, o seu primeiro papel foi em Orgulho e Preconceito. Imagina só, então, se o episódio piloto tivesse sido mantido e ela fosse a nossa Daenerys?!</p>
